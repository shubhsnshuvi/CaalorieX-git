// Function to upload food items to Firestore
async function uploadFoodsToFirestore(foods: DetailedFoodItem[]) {
  console.log(`Starting upload of ${foods.length} food items to Firestore...`)

  const batchSize = 500
  const batches = Math.ceil(foods.length / batchSize)

  for (let i = 0; i < batches; i++) {
    const start = i * batchSize
    const end = Math.min(start + batchSize, foods.length)
    const batch = foods.slice(start, end)

    console.log(`Processing batch ${i + 1}/${batches} (${batch.length} items)...`)

    const promises = batch.map(async (food) => {
      try {
        const docRef = db.collection("ifct_foods").doc(food.id)
        await docRef.set(food)
        return { success: true, id: food.id }
      } catch (error) {
        console.error(`Error uploading food item ${food.id}:`, error)
        return { success: false, id: food.id, error }
      }
    })

    const results = await Promise.all(promises)
    const successful = results.filter((r) => r.success).length
    const failed = results.filter((r) => !r.success).length

    console.log(`Batch ${i + 1} completed: ${successful} successful, ${failed} failed`)
  }

  console.log(`Finished uploading food items to Firestore.`)
}

